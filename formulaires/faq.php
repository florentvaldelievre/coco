<h2>Réponses Questions générales</h2>




	<a name="site"></a>
<div class="faqEntryReponse">A quoi sert le site ?</div>
<div class="faqEntry">
	<ul>
		<li>Notre site permet de mettre en relation des voyageurs recherchant un ou des bus pour effectuer un voyage, une balade, avec différents transporteurs afin d'en tirer le meilleur prix.</li>
	</ul>
</div>




	<a name="commentcamarche"></a>
	
	<div class="faqEntryReponse">Comment le site fonctionne ?</div>
<div class="faqEntry">
	<ul>
		<li>Dotée d'une base de données riche en transporteurs cotés, les voyageurs postent une annonce de demande de transport et attendent une proposition de prix de la part des transporteurs. Le voyageur à juste à sélectionner le transporteur qui lui convient le mieux.</li>
		<li>Pour mieux comprendre le fonctionnement, voici un <a href="index.php?action=commentcamarche">schéma récapitulatif</a>
	</ul>
</div>



	<a name="notreserviceClient"></a>
	
	<div class="faqEntryReponse">Pourquoi utiliser notre service en tant que <strong>voyageur</strong> ?</div>
<div class="faqEntry">
	<ul>
		<li>En tant que voyageur, notre service vous permet de mettre en concurrence les différents transporteurs par un système de sous enchères.
Vous aurez juste à choisir le transporteur qui réponds le mieux à vos critères ( prix, quotation etc.)</li>
		<li> Le service est totalement gratuit, aucune commission n'est demandée. </li>
	</ul>
</div>


	<a name="notreserviceTransporteur"></a>
	
	<div class="faqEntryReponse">Pourquoi utiliser notre service en tant que <strong>transporteur</strong> ?</div>
<div class="faqEntry">
	<ul>
		<li>Si vous êtes transporteur, notre service permet d'élargir votre clientèle facilement grâce aux annonces des voyageurs et ainsi, augmenter votre popularité.</li>
	</ul>
</div>










<h2>Réponses relatives à l'autocariste</h2>


	<h3>Profil</h3>
	

	<a name="informations_contact_tr"></a>
	
	<div class="faqEntryReponse">Pourquoi dois-je remplir des informations me concernant(Telephone, Mail etc.) ?</div>
<div class="faqEntry">
	<ul>
		<li>Dès lors qu'un voyageur à validé votre offre, vous êtes immédiatement mis en contact par ces informations. A cet effet il est extrêmement important de bien remplir ces champs.</li>
	</ul>
</div>


	<a name="informations_societe_tr"></a>
	
	<div class="faqEntryReponse">Pourquoi dois-je remplir des informations concernant ma société ?</div>
<div class="faqEntry">
	<ul>
		<li>Lorsque vous avez fait une offre, le client est en mesure de voir ces différentes informations de votre profil afin de l'aider à choisir.</li>
	</ul>
</div>


	<a name="profilnom_tr"></a>
	
	<div class="faqEntryReponse">Pourquoi mon nom est t'il grisé dans l'édition du profil ?</div>
<div class="faqEntry">
	<ul>
		<li>Pour que le voyageur vous reconnaisse et puisse vous choisir, il est indispensable que votre nom reste le même. C'est pour cela que nous ne vous offrons pas la possibilité de le changer.</li>
	</ul>
</div>


	<a name="departements_tr"></a>
	
	<div class="faqEntryReponse">A quoi sert de remplir la prise en charge des départements ?</div>
<div class="faqEntry">
	<ul>
		<li>Il est possible de filtrer automatiquement les annonces en fonction de vos départements pris en charge. Pour cela il suffit de cliquer sur lien 'Les annonces que Waybus a sélectionné pour vous'.</li>
	</ul>
</div>




	<h3>Paiement</h3>


	<a name="commission_tr"></a>
	
	<div class="faqEntryReponse">A quel taux s'élève la commission ?</div>
<div class="faqEntry">
	<ul>
		<li>La commission est de <strong>5%</strong>. Ce qui veut dire que si le voyageur accepte votre offre à 1000€, vous nous devrez 50€.</li>
	</ul>
</div>


	<a name="moyenspaiement_tr"></a>
	
	<div class="faqEntryReponse">Quels sont les moyens de paiements autorisés ?</div>
<div class="faqEntry">
	<ul>
		<li>Il est possible de payer par Carte bancaire via le site de Paypal. Vous pouvez accélérer cette procédure en créant un compte paypal. Les transactions effectuées sur Paypal sont entièrement sécurisées(SSL).</li>
	</ul>
</div>


	<a name="procederpaiement_tr"></a>
	
	<div class="faqEntryReponse">Comment procéder au paiement ?</div>
<div class="faqEntry">
	<ul>
		<li><strong>Attention</strong>, Il est fortement conseillé de procéder au paiement lorsque le voyageur vous a versé la somme qu'il vous devait.</li>
		<li>Pour payer, il suffit de vous rendre dans le menu [Mes Demandes/Validées] puis de cliquer sur le billet vert <img alt="Payer par Paypal (sécurisé)" title="Payer par Paypal (sécurisé)" src="images/style1/money.gif"/></li>
	</ul>
</div>

	<a name="paiementwaybus_tr"></a>
	
	<div class="faqEntryReponse">Quand dois-je payer waybus ?</div>
<div class="faqEntry">
	<ul>
		<li>Vous avez un mois à compter de la date de fin du voyage.</li>
	</ul>
</div>



				
